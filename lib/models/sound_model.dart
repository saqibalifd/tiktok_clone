class SoundModel {
  final String soundId;
  final String name;
  final String author;
  final String soundUrl;
  final int videosCount;
  final double duration;

  SoundModel({
    required this.soundId,
    required this.name,
    required this.author,
    required this.soundUrl,
    required this.videosCount,
    required this.duration,
  });
}
